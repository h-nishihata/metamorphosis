#include "TestLayer3.h"

//--------------------------------------------------------------------------------------------------------------
void TestLayer3::setup(){

    img.loadImage("ikuo.jpg");
    x = -10;
    y = -10;
    speedX = 0.1;
    speedY = 0.1;
    
}
//--------------------------------------------------------------------------------------------------------------
void TestLayer3::update(){
    
    x += speedX;
    y += speedY;
    
    if (x >= 0 || x <= -550) {
        speedX = speedX*-1;
    }
    if (y >= 0 || y <= -50) {
        speedY = speedY*-1;
    }
    
}
//--------------------------------------------------------------------------------------------------------------
void TestLayer3::draw(){

    ofEnableAlphaBlending();
    ofBackground(255,255,255,0);
    
    ofSetColor(245,229,171);
    img.draw(x, y, 1990, 950);
    
}
